package airlines;

/**
 *
 * @author DELL
 */
public class Country {
    private double Currency;
private String Hotel;
private String Restaurant;
private String TouristPlaces;
private String PopularFood;

    public Country() {
    }

    public String getHotel() {
        return Hotel;
    }

    public void setHotel(String Hotel) {
        this.Hotel = Hotel;
    }

    public String getRestaurant() {
        return Restaurant;
    }

    public void setRestaurant(String Restaurant) {
        this.Restaurant = Restaurant;
    }

    public String getTouristPlaces() {
        return TouristPlaces;
    }

    public void setTouristPlaces(String TouristPlaces) {
        this.TouristPlaces = TouristPlaces;
    }

    public String getPopularFood() {
        return PopularFood;
    }

    public void setPopularFood(String PopularFood) {
        this.PopularFood = PopularFood;
    }

    public double getCurrency() {
        return Currency;
    }

    public void setCurrency(double Currency) {
        this.Currency = Currency;
    }

}
